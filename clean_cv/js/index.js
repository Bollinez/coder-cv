function getDepth(obj){
  if(!obj || obj.length===0 || typeof(obj)!=="object") return 0;
  const keys = Object.keys(obj);
  let depth = 0;
  keys.forEach(key=>{
      let tmpDepth = getDepth(obj[key]);
      if(tmpDepth>depth){
          depth = tmpDepth;
      }
  })
  return depth+1;
}


